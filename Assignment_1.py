#Let the variables be x & y
x = int(input("The value of x is: "))

y = float(input("The value of y is: "))

#Addition of x and y
sum = x + y

#Subtraction of x and y
difference = x - y

#Multiplication of x and y
product = x * y

#Division of x and y
quotient = x / y

print(f'The Sum of x & y : ', x," + ",y, " = ",sum,'\n'f'The Difference of x & y : ', x," - ",y, " = ",difference,'\n'f'The Product of x & y : ', x," * ",y, " = ",product,'\n'f'The Quotient of x & y : ', x," / ",y, " = ",quotient)

